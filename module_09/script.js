'use strict';

const startBtn = document.querySelector('.js-start');
const lapBtn = document.querySelector('.js-take-lap');
const resetBtn = document.querySelector('.js-reset');
const clockFace = document.querySelector('.js-time');
let list;

// –Ω–∞—à –æ–±—ä–µ–∫—Ç —Ç–∞–π–º–µ—Ä
const timer = {
    id: null,
    startTime: null,
    isActive: false,
    deltaTime: 0,
    lapsCounter: [],
    start(){
        if(this.isActive) return;

        this.isActive = true;
        this.startTime = Date.now() - this.deltaTime;
        this.id = setInterval(() => {
            const currentTime = Date.now();
            this.deltaTime = currentTime - this.startTime;
            updateClockFace(this.deltaTime);
        }, 100)
    },
    stop(){
        clearInterval(this.id);
        this.isActive = false;
    },
    reset(){
        clearInterval(this.id);
        this.deltaTime = 0;
        updateClockFace(this.deltaTime);
        this.isActive = false;
    },
    lap(){
        const lapTime = getFormattedTime(this.deltaTime);
        this.lapsCounter.push(lapTime);
        
        const ul = document.querySelector('.js-laps');

        const li = document.createElement('li');
        list = ul;
        li.textContent = this.lapsCounter[this.lapsCounter.length -1];
        li.style.width = '500px';
        li.style.fontSize = '50px';
        li.style.textAlign = 'center';
        ul.appendChild(li);
        return ul;
    }
};

//—Å–ª—É—à–∞—Ç–µ–ª–∏ –∫–ª–∏–∫–æ–≤ –ø–æ –∫–Ω–æ–ø–∫–∞–º
startBtn.addEventListener('click', handleStartBtnClick);
lapBtn.addEventListener('click', handleLapBtnClick);
resetBtn.addEventListener('click', handleResetBtnClick);

// –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–ª—É—à–∞—Ç–µ–ª–µ–π –∫–ª–∏–∫–æ–≤
function handleStartBtnClick(){
    if(!timer.isActive){
        timer.start();
        this.textContent = 'Pause';
        setActiveBtn(this);
    } else{
        timer.stop();
        this.textContent = 'Continue';
        this.classList.remove('active');
    };
}
function handleResetBtnClick(){
    timer.reset();
    startBtn.textContent = 'Start';
    startBtn.classList.remove('active');
    list.textContent = '';
    setActiveBtn(this);
}

function handleLapBtnClick(){
   timer.lap();
   this.classList.add('active');
}

// –û–±–Ω–æ–≤–ª—è–µ—Ç –ø–æ–ª–µ —Å—á–µ—Ç—á–∏–∫–∞ –Ω–æ–≤—ã–º –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø—Ä–∏ –≤—ã–∑–æ–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç time —ç—Ç–æ –∫–æ–ª-–≤–æ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥
function updateClockFace (time) {
    clockFace.textContent = getFormattedTime(time);
}

// –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –æ—Å—á–∏—Ç–∞–Ω–Ω–æ–≥–æ –≤—Ä–µ–º–µ–Ω–∏ –≤ –Ω—É–∂–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç
function getFormattedTime(time) {
  const date = new Date(time);
  let min = date.getMinutes();
  min = min < 10 ? `0${min}` : min;
  let sec = date.getSeconds();
  sec = sec < 10 ? `0${sec}` : sec;
  let milliSec = Number.parseInt(date.getMilliseconds() / 100);
  return`${min}:${sec}.${milliSec}`;
}

// –ü–æ–¥—Å–≤–µ—Ç–∫–∞ –∞–∫—Ç–∏–≤–Ω–æ–π –∫–Ω–æ–ø–∫–∏
function setActiveBtn(button) {
  if(button.classList.contains('active')) {
    lapBtn.classList.remove('active');
    resetBtn.classList.remove('active');
    return;
  }
  
  lapBtn.classList.remove('active');
  resetBtn.classList.remove('active');
  button.classList.add('active');
}

